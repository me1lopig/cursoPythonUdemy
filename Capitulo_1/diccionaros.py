# ejemplo de uso de los diccionarios

diccionario1={'España':'Madrid','Francia':'Paris','Italia':'Roma'}
print(diccionario1)

# imprimir las claves
print(diccionario1.keys())

# imprimir los valores
print(diccionario1.values())

#indicar el número de valores del diccionario
print('El numero de valores del diccionario es  ',len(diccionario1))

# borrado de un elemento
del diccionario1 ['Francia'] # eliminamos un elemento cualquiera de la lista
print(diccionario1)




